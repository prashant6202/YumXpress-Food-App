    private void showFoodDetailsByIndex(int foodIndex) {
         product=foodList.get(foodIndex);
        String foodName=product.getProductName();
        if(foodName.length()>=50){
        foodName=foodName.substring(0,45)+"...";
        
        }
        this.txtFoodName.setText(foodName);
        
        Image img=product.getProductImage();
        img=img.getScaledInstance(this.lblFoodImage.getWidth(),this.lblFoodImage.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon icon=new ImageIcon(img);
        this.lblFoodImage.setIcon(icon);      
    }

    private boolean validateInputs(){
    if(this.txtFoodName.getText().trim().isEmpty()||this.txtFoodPrice.getText().trim().isEmpty()||this.lblFoodImage.getIcon()==null){
    return false;
    }
    return true;
    }

