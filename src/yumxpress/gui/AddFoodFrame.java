    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
       String searchFood=this.txtSearchFood.getText().trim();
       if(searchFood.isEmpty()){
          JOptionPane.showMessageDialog(null,"Plese type a food name");
          return;
       }
       try{
           
           searchFood=searchFood.replaceAll(" ", "-");
           foodList= SpoonacularAPI.getAllItemDetailsByName(searchFood);
           if(foodList.isEmpty()){
               JOptionPane.showMessageDialog(null,"No food details for "+searchFood+" found");
               return;
           }
           this.lblNext.setEnabled(true);
           this.lblBack.setEnabled(true);
           foodIndex=0;
           showFoodDetailsByIndex(foodIndex);   
       }catch(Exception ex){
       JOptionPane.showMessageDialog(null,"Exception while using API");
       ex.printStackTrace();
       }
    }//GEN-LAST:event_btnSearchActionPerformed

    private void lblNextMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblNextMouseClicked

        foodIndex++;
       if(foodIndex>=foodList.size()){
       foodIndex=0;
       }
       showFoodDetailsByIndex(foodIndex);
    }//GEN-LAST:event_lblNextMouseClicked

    private void lblBackMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblBackMouseClicked
        foodIndex--;
        if(foodIndex<0){
        foodIndex=foodList.size()-1;
        }
        showFoodDetailsByIndex(foodIndex);
    }//GEN-LAST:event_lblBackMouseClicked

    private void btnAddItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddItemActionPerformed
        if(!validateInputs()){
        JOptionPane.showMessageDialog(null, "Plese fill all the data");
        return;
        }  
        try{
        double price=Double.parseDouble(txtFoodPrice.getText().trim());
        product.setCompanyId(OwnerProfile.getCompanyId());
        product.setProductName(txtFoodName.getText().trim());
        product.setProductPrice(price);
        boolean result=ProductDAO.addProduct(product);
        if(result==false){
        JOptionPane.showMessageDialog(null,"Can not add the product");
        return;
        }
        JOptionPane.showMessageDialog(null,"Product successfully added");
        foodList.remove(foodIndex);
        if(foodList.isEmpty()){
        JOptionPane.showMessageDialog(null,"All varities of "+this.txtSearchFood.getText().trim()+" added");
        clearAll();
        return;
        }
        foodIndex=0;
        this.showFoodDetailsByIndex(foodIndex);
        
         
        }catch(SQLException ex){
        JOptionPane.showMessageDialog(null,"Exception in database in AddFoodFrame");
        ex.printStackTrace();
       }catch(NumberFormatException ex){
        JOptionPane.showMessageDialog(null,"Plese input numeric value for price");
        ex.printStackTrace();
       }catch(IOException ex){
        JOptionPane.showMessageDialog(null,"Exception in DAO image processing in addFoodFrame");
        ex.printStackTrace();
       }
    }//GEN-LAST:event_btnAddItemActionPerformed

