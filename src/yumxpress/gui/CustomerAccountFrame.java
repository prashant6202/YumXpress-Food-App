     private boolean validateInputs() {
        char[]pwd=  this.txtPassword.getPassword();
      if(pwd.length==0||this.txtCustomerName.getText().trim().isEmpty()||this.txtCustomerAddress.getText().trim().isEmpty()||this.txtCustomerEmailId.getText().trim().isEmpty()||this.txtCustomerAddress.getText().trim().isEmpty()||this.txtCustomerMobileNo.getText().trim().isEmpty()){
     JOptionPane.showMessageDialog(null,"Plese fill all details");
     return false;
     }
     
     if(Validator.isValidMobileNo(this.txtCustomerMobileNo.getText().trim())==false){
      JOptionPane.showMessageDialog(null,"Plese fill valid Mobile Number");
      return false;
     }
     return true;
    }

    
