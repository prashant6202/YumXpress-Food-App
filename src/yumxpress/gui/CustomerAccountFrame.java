    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        boolean result=validateInputs();
      if(!result){
      return;
      }
       try{
      customer.setCustomerName(this.txtCustomerName.getText().trim());
      customer.setMobileNo(this.txtCustomerMobileNo.getText().trim());
      customer.setAddress(this.txtCustomerAddress.getText().trim());
      char[]pwd= this.txtPassword.getPassword();
      String pwdstr=String.valueOf(pwd);
      pwdstr=PasswordEncryption.getEncryptedPassword(pwdstr);
      customer.setPassword(pwdstr);
       
     boolean isDataAdded=CustomerDAO.updateCustomer(customer);
             if(isDataAdded){
       JOptionPane.showMessageDialog(null, "Customer updated successfull..");
       clearAll();
       return;
             }
       }catch(SQLException ex){
        JOptionPane.showMessageDialog(null, "Database error in CustomerAccountFrame");
        ex.printStackTrace();
       }
    }//GEN-LAST:event_btnUpdateActionPerformed

