    private void loadCustomerDetailsById(String userId) {
         try{
               customer=CustomerDAO.getCustomerDetailsById(userId);
               this.txtCustomerName.setText(customer.getCustomerName().trim());
               String password=PasswordEncryption.getDecryptedPassword(customer.getPassword().trim());
               this.txtPassword.setText(password);
               this.txtCustomerMobileNo.setText(customer.getMobileNo().trim());
               this.txtCustomerAddress.setText(customer.getAddress().trim());     
        }catch(SQLException ex){
        JOptionPane.showMessageDialog(null, "Database error in CustomerAccount Frame");
        ex.printStackTrace();
        }
    }

    private void clearAll() {
        this.txtCustomerName.setText("");
        this.txtCustomerMobileNo.setText("");
        this.txtCustomerAddress.setText("");
        this.txtPassword.setText("");
        this.txtCustomerName.requestFocus();
    }
