    private boolean validateInputs() {
        char[]pwd=  this.jPasswordField1.getPassword();
     if(pwd.length==0||this.txtCustomerrName.getText().trim().isEmpty()||this.txtCustomerAddress.getText().trim().isEmpty()||this.txtCustomerEmailId.getText().trim().isEmpty()||this.txtCustomerAddress.getText().trim().isEmpty()||this.txtCustomerMobileNo.getText().trim().isEmpty()){
     JOptionPane.showMessageDialog(null,"Plese fill all details");
     return false;
     }
     if(Validator.isValidEmail(this.txtCustomerEmailId.getText().trim())==false){
         JOptionPane.showMessageDialog(null,"Plese fill valid email id");
     return false;
     }
     if(Validator.isValidMobileNo(this.txtCustomerMobileNo.getText().trim())==false){
      JOptionPane.showMessageDialog(null,"Plese fill valid Mobile Number");
      return false;
     }
     return true;
    }

