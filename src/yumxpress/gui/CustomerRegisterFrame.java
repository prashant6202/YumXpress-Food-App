           emailCredentials.put("securityKey","suzy lohi dikq cojf");
          Mailer.sendMail(emailCredentials, customer);
          JOptionPane.showMessageDialog(null,"Mail sent to "+customer.getEmailId()+" successfully from YUMXPRESS APP");
