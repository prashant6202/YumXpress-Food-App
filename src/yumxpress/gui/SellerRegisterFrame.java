    private boolean validateInputs() {
      char[]pwd=  this.jPasswordField1.getPassword();
     if(pwd.length==0||this.txtCompanyName.getText().trim().isEmpty()||this.txtOwnerName.getText().trim().isEmpty()||this.txtEmailId.getText().trim().isEmpty()||this.txtSecurityKey.getText().trim().isEmpty()){
     JOptionPane.showMessageDialog(null,"Plese fill all details");
     return false;
     }
     if(Validator.isValidEmail(this.txtEmailId.getText().trim())==false){
         JOptionPane.showMessageDialog(null,"Plese fill valid email id");
     return false;
     }
     return true;
    }

