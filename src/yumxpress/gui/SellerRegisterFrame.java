    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        boolean result=validateInputs();
        if(!result){
        return;
        }
        
        try{
        CompanyPojo comp=new CompanyPojo();
        comp.setCompanyName(this.txtCompanyName.getText().trim());
        comp.setOwnerName(this.txtOwnerName.getText().trim()); 
       comp.setEmailId(this.txtEmailId.getText().trim());
       comp.setSecurityKey(this.txtSecurityKey.getText().trim());
       
       char[]pwd= this.jPasswordField1.getPassword();
       String pwdstr=String.valueOf(pwd);
        pwdstr=PasswordEncryption.getEncryptedPassword(pwdstr);
        comp.setPassword(pwdstr);
       
       boolean isDataAdded=CompanyDAO.addSeller(comp);
       if(isDataAdded){
       JOptionPane.showMessageDialog(null, "Registeration successfull..");
            clearAll();
            return;
       }else{
       JOptionPane.showMessageDialog(null, "Registeration  unsuccessfull..");
       }
       
        }catch(SQLException ex){
        JOptionPane.showMessageDialog(null, "Database error in Seller Registration Frame");
        ex.printStackTrace();
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

